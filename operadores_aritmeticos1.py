# Operaciones aritméticas básicas
print(f"6 + 6 = {6 + 6}   # Suma")
print(f"6 - 6 = {6 - 6}   # Resta")
print(f"6 * 6 = {6 * 6}   # Multiplicación")
print(f"6 / 6 = {6 / 6}   # División flotante")
print(f"6 % 6 = {6 % 6}   # Módulo (resto de la división)")
print(f"6 ** 6 = {6 ** 6}  # Exponenciación")
print(f"6 // 6 = {6 // 6}  # División entera")

